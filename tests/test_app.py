def test_runbook_execution_rejects_unrelated_service_and_incident() -> None:
    with _client() as client:
        incident_service = _create_service(client, slug="incident-service")
        other_service = _create_service(client, slug="unrelated-service")
        incident_response = client.post(
            "/api/incidents",
            json={
                "service_id": incident_service["id"],
                "title": "Service-specific incident",
                "severity": "high",
            },
        )
        incident = incident_response.json()

        execution_response = client.post(
            "/api/runbooks/generate_incident_report/execute",
            json={
                "service_id": other_service["id"],
                "incident_id": incident["id"],
                "requested_by": "pytest",
            },
        )
        logs_response = client.get("/api/audit-logs")

    assert execution_response.status_code == 200
    execution = execution_response.json()
    assert execution["status"] == "failed"
    assert execution["service_id"] is None
    assert execution["incident_id"] == incident["id"]
    assert "does not match" in execution["output"]
    assert any(
        log["action"] == "runbook.executed"
        and log["entity_id"] == execution["id"]
        and '"status": "failed"' in log["details"]
        for log in logs_response.json()
    )


def test_runbook_execution_creates_audit_log() -> None:
    with _client() as client:
        execution_response = client.post(
            "/api/runbooks/simulate_backup_check/execute",
            json={"requested_by": "pytest"},
        )
        execution = execution_response.json()
        logs_response = client.get("/api/audit-logs")

    assert execution_response.status_code == 200
    assert logs_response.status_code == 200
    assert any(
        log["action"] == "runbook.executed"
        and log["entity_type"] == "RunbookExecution"
        and log["entity_id"] == execution["id"]
        for log in logs_response.json()
    )
