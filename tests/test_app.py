def test_create_incident_rejects_service_conflicting_with_source_alert() -> None:
    with _client() as client:
        alert_service = _create_service(client, slug="alert-service")
        other_service = _create_service(client, slug="other-service")
        alert_response = client.post(
            "/api/alerts",
            json={
                "service_id": alert_service["id"],
                "source": "pytest",
                "title": "Critical alert",
                "message": "A critical alert generated by the test suite.",
                "severity": "critical",
            },
        )
        alert = alert_response.json()

        response = client.post(
            "/api/incidents",
            json={
                "service_id": other_service["id"],
                "source_alert_id": alert["id"],
                "title": "Conflicting incident",
                "severity": "high",
            },
        )

    assert response.status_code == 409
    assert response.json()["detail"] == "Incident service must match the source alert service."


