def test_create_and_complete_manual_runbook() -> None:
    with _client() as client:
        service = _create_service(client, slug="manual-runbook-service")
        incident = _create_manual_incident(client, service["id"])
        runbook_response = client.post(
            "/api/runbooks",
            json={
                "key": "verify_demo_dependency",
                "name": "Vérifier une dépendance de démonstration",
                "description": "Manual test runbook.",
                "mode": "manual",
                "instructions": "Confirm each step.",
                "steps": ["Check context", "Record result"],
                "required_context": "incident",
                "risk_level": "low",
            },
            headers={"X-OpsForge-Actor": "pytest"},
        )
        runbook = runbook_response.json()
        execution_response = client.post(
            f"/api/runbooks/{runbook['key']}/execute",
            json={
                "incident_id": incident["id"],
                "requested_by": "pytest",
                "outcome": "success",
                "notes": "Dependency is healthy.",
                "completed_steps": [0, 1],
            },
        )
        logs = client.get("/api/audit-logs").json()

    assert runbook_response.status_code == 201
    assert runbook["steps"] == ["Check context", "Record result"]
    assert execution_response.status_code == 200
    assert execution_response.json()["status"] == "success"
    assert execution_response.json()["output"] == "Dependency is healthy."
    assert any(
        log["action"] == "incident.runbook_executed"
        and log["entity_id"] == incident["id"]
        for log in logs
    )


