def test_incident_transitions_are_forward_only_and_audited() -> None:
    with _client() as client:
        service = _create_service(client, slug="status-service")
        incident = _create_manual_incident(client, service["id"])
        direct_resolve = client.patch(
            f"/api/incidents/{incident['id']}/status", json={"status": "resolved"}
        )
        investigating = client.patch(
            f"/api/incidents/{incident['id']}/status", json={"status": "investigating"}
        )
        resolved = client.patch(
            f"/api/incidents/{incident['id']}/status", json={"status": "resolved"}
        )
        reopened = client.patch(
            f"/api/incidents/{incident['id']}/status", json={"status": "investigating"}
        )
        logs = client.get("/api/audit-logs").json()

    assert direct_resolve.status_code == 409
    assert investigating.json()["status"] == "investigating"
    assert resolved.json()["status"] == "resolved"
    assert resolved.json()["resolved_at"] is not None
    assert reopened.status_code == 409
    assert sum(
        log["action"] == "incident.status_changed" and log["entity_id"] == incident["id"]
        for log in logs
    ) == 2


